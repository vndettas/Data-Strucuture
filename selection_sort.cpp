template<typename T>
void 
selection_sort(std::vector<T>& vector)
{

  for(int i = 0; i < vector.size(); i++){
    T min = vector.at(i);
    uint32_t  min_index = i;
    for(int j = i; j < vector.size(); j++){
      if(vector.at(j) < min){
        min = vector.at(j);
        min_index = j;
      }
    }
    if(vector.at(i) != min){
      std::iter_swap(vector.begin() + i, vector.begin() + min_index);
    }
  }
}

